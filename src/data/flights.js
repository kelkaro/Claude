/**
 * Generate mock price-by-date data (for flexible date calendar).
 * Returns Array<{ date: string, price: number, cheapest: boolean }>
 */
export function generateDatePrices(basePrice, startDateStr, days = 30) {
  const result = [];
  // seed-based pseudo-random for determinism
  let seed = basePrice * 13;
  const rng = () => { seed = (seed * 9301 + 49297) % 233280; return seed / 233280; };

  for (let i = 0; i < days; i++) {
    const d = new Date(startDateStr);
    d.setDate(d.getDate() + i);
    const dow = d.getDay();
    const weekendFactor = dow === 0 || dow === 6 ? 1.15 : 1.0;
    const price = Math.round(basePrice * weekendFactor * (0.80 + rng() * 0.40));
    result.push({ date: d.toISOString().split('T')[0], price, cheapest: false });
  }
  const min = Math.min(...result.map((r) => r.price));
  result.forEach((r) => { r.cheapest = r.price === min; });
  return result;
}
